import { sql, isNull, eq, and } from "drizzle-orm";
import { logger } from "./logger";

export async function seedIfEmpty(): Promise<void> {
  const [{ count: pathwayCount }] = await db
    .select({ count: sql<number>`count(*)::int` })
    .from(pathwayItemsTable);

  if (pathwayCount === 0) {
    await db.insert(pathwayItemsTable).values(DEFAULT_PATHWAY_ITEMS);
    logger.info({ inserted: DEFAULT_PATHWAY_ITEMS.length }, "Seeded pathway items");
  } else {
    // Additive seeding so newly-added default progression steps appear without duplicates.
    const existingDefaults = await db
      .select({
        keyStage: pathwayItemsTable.keyStage,
        yearGroup: pathwayItemsTable.yearGroup,
        strand: pathwayItemsTable.strand,
        title: pathwayItemsTable.title,
      })
      .from(pathwayItemsTable)
      .where(and(eq(pathwayItemsTable.isCustom, 0), isNull(pathwayItemsTable.teacherId)));
    const key = (k: string, y: string, s: string, t: string) => `${k}|${y}|${s}|${t}`;
    const have = new Set(existingDefaults.map((r) => key(r.keyStage, r.yearGroup, r.strand, r.title)));
    const missing = DEFAULT_PATHWAY_ITEMS.filter((p) => !have.has(key(p.keyStage, p.yearGroup, p.strand, p.title)));
    if (missing.length > 0) {
      await db.insert(pathwayItemsTable).values(missing);
      logger.info({ inserted: missing.length }, "Seeded missing default pathway items");
    }
  }

  // Activities: additive seeding so newly-added defaults (e.g. Mr Meehl video links)
  // are inserted on subsequent boots without duplicating existing default rows.
  const ALL_DEFAULTS = [
    ...DEFAULT_ACTIVITIES,
    ...MEEHL_ACTIVITIES,
    ...EYFS_ACTIVITIES,
    ...KODALYHUB_ACTIVITIES,
    ...TREBLE_CLASSROOM_ACTIVITIES,
  ];
  const existing = await db
    .select({ title: activitiesTable.title })
    .from(activitiesTable)
    .where(isNull(activitiesTable.teacherId));
  const existingTitles = new Set(existing.map((r) => r.title));
  const toInsert = ALL_DEFAULTS.filter((a) => !existingTitles.has(a.title));
  if (toInsert.length > 0) {
    await db.insert(activitiesTable).values(toInsert);
    logger.info({ inserted: toInsert.length }, "Seeded missing default activities");
  }

  // Backfill youtubeLink on existing default rows whose link is still empty.
  const linkUpdates = ALL_DEFAULTS.filter((a) => a.youtubeLink && existingTitles.has(a.title));
  for (const a of linkUpdates) {
    await db
      .update(activitiesTable)
      .set({ youtubeLink: a.youtubeLink })
      .where(and(
        eq(activitiesTable.title, a.title),
        isNull(activitiesTable.teacherId),
        eq(activitiesTable.youtubeLink, ""),
      ));
  }

  const [{ count: curCount }] = await db
    .select({ count: sql<number>`count(*)::int` })
    .from(curriculumLinksTable);
  if (curCount === 0) {
    await db.insert(curriculumLinksTable).values(DEFAULT_CURRICULUM_LINKS);
    logger.info({ inserted: DEFAULT_CURRICULUM_LINKS.length }, "Seeded curriculum links");
  }

  // Resources — additive seeding for global (teacherId NULL) defaults.
  const existingResources = await db
    .select({ url: resourcesTable.url })
    .from(resourcesTable)
    .where(isNull(resourcesTable.teacherId));
  const existingResourceUrls = new Set(existingResources.map((r) => r.url));
  const resourcesToInsert = DEFAULT_RESOURCES.filter((r) => !existingResourceUrls.has(r.url));
  if (resourcesToInsert.length > 0) {
    await db.insert(resourcesTable).values(resourcesToInsert);
    logger.info({ inserted: resourcesToInsert.length }, "Seeded default resources");
  }
}

